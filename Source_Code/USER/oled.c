void OLED_WrDat(unsigned f,int data)
{
     int i=8;
     OLED_DC_1;
     OLED_SCL_0;
     if(f==0)
     {
       while(i--)
      {
         if(data&0x80)
           OLED_SDA_1;
         else
           OLED_SDA_0;

         OLED_SCL_1;;
         OLED_SCL_0;;
         data<<=1;
       }
     }
     else
     {
        while(i--)
          {
            if(data&0x80)
              OLED_SDA_0;
            else
               OLED_SDA_1;

             OLED_SCL_1;;
             OLED_SCL_0;;
             data<<=1;
           }

     }
}

void OLED_WrCmd(int cmd)
{
     int i=8;
     OLED_DC_0;
     OLED_SCL_0;
     while(i--)
     {
       if(cmd&0x80)
         OLED_SDA_1;
       else
         OLED_SDA_0;

       OLED_SCL_1;
       OLED_SCL_0;
       cmd<<=1;
     }
}

void OLED_Set_Pos(int x,int y)
{
     OLED_WrCmd(0xb0+y);
     OLED_WrCmd(((x&0xf0)>>4)|0x10);
     OLED_WrCmd((x&0x0f)|0x01);
}

void OLED_Fill(int bmp_data)
{
     int y,x;
     for(y=0;y<8;y++)
     {
        OLED_WrCmd(0xb0+y);
        OLED_WrCmd(0x01);
        OLED_WrCmd(0x10);
        for(x=0;x<X_WIDTH;x++)
            OLED_WrDat(0,bmp_data);
     }
}

